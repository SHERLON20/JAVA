    private boolean validar(){
        if(camposenha.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "error! deve preencher todos os campos");
            return false;
                }else if (camponome.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "error! deve preencher todos os campos");
                return false;}else if(campoemail.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "error! deve preencher todos os campos");
        return false;}else if (campologin.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "error! deve preencher todos os campos");

        return false;}
        return true;
        
    }
    
